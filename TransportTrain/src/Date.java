public class Date{
    private int day;
    private int month;
    private int year;
    private int hour;
    private int min;

    public int getDay(){
        return this.day;
    }

    public int getMonth(){
        return this.month;
    }

    public int getYear(){
        return this.year;
    }

    public int getHour(){
        return this.hour;
    }

    public int getMin(){
        return this.min;
    }

    public void setDay(int day){
        this.day = day;
    }

    public void setMonth(int month){
        this.month = month;
    }

    public void setYear(int year){
        this.year = year;
    }

    public void setHour(int hour){
        this.hour = hour;
    }

    public void setMin(int min){
        this.min = min;
    }
    
    
}
